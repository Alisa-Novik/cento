def is_external(href: str) -> bool:
    return href.startswith(("http://", "https://", "mailto:", "#"))


def referenced_links(manifest: dict[str, Any]) -> list[str]:
    links: list[str] = []
    for section in ("use_first", "validation_results", "screenshots"):
        for item in manifest.get(section) or []:
            href = str(item.get("href") or "")
            if href:
                links.append(href)
    return links


def validate_links(manifest_path: Path, manifest: dict[str, Any]) -> list[str]:
    base = manifest_path.parent
    missing = []
    for href in referenced_links(manifest):
        if is_external(href):
            continue
        target = (base / href).resolve()
        if not target.exists():
            missing.append(href)
    return missing


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description="Generate and validate a stable HTML deliverables hub from a story manifest.")
    parser.add_argument("manifest", type=Path, help="Path to deliverables.json.")
    parser.add_argument("--output", type=Path, default=None, help="Output HTML path. Defaults to <manifest-dir>/start-here.html.")
    parser.add_argument("--check-links", action="store_true", help="Fail if local links in the manifest do not exist.")
    parser.add_argument("--check-only", action="store_true", help="Validate manifest and links without writing HTML.")
    return parser


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    manifest_path = args.manifest if args.manifest.is_absolute() else (ROOT / args.manifest)
    output = args.output or (manifest_path.parent / "start-here.html")
    if not output.is_absolute():
        output = ROOT / output

    manifest = load_manifest(manifest_path)
    missing = validate_links(manifest_path, manifest) if args.check_links else []
    if missing:
        print("missing local deliverable links:", file=sys.stderr)
        for href in missing:
            print(f"- {href}", file=sys.stderr)
        return 1

    if not args.check_only:
        output.write_text(render(manifest), encoding="utf-8")
        print(output)
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
