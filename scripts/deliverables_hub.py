def esc(value: Any) -> str:
    return html.escape(str(value or ""), quote=True)


def badge_slug(value: Any) -> str:
    slug = re.sub(r"[^a-z0-9]+", "-", str(value or "").strip().lower()).strip("-")
    return slug or "neutral"


def badge_text(value: Any) -> str:
    text = str(value or "").strip()
    if not text:
        return ""
    return re.sub(r"[-_]+", " ", text).upper()


def load_manifest(path: Path) -> dict[str, Any]:
    try:
        payload = json.loads(path.read_text(encoding="utf-8"))
    except FileNotFoundError as exc:
        raise SystemExit(f"manifest not found: {path}") from exc
    except json.JSONDecodeError as exc:
        raise SystemExit(f"invalid JSON in {path}: {exc}") from exc
    if not isinstance(payload, dict):
        raise SystemExit("manifest root must be an object")
    return payload


def cards(items: list[dict[str, Any]]) -> str:
    output = []
    for item in items:
        href = esc(item.get("href") or "#")
        class_name = "card primary" if item.get("primary") else "card"
        title = esc(item.get("title"))
        description = esc(item.get("description"))
        code = item.get("code")
        badge = item.get("badge")
        lines = [
            f'          <a class="{class_name}" href="{href}">',
        ]
        if badge:
            lines.append(
                f'            <div class="card-badge"><span class="pill pill-{badge_slug(badge)}">{esc(badge_text(badge))}</span></div>'
            )
        lines.append(f"            <h3>{title}</h3>")
        if code:
            lines.append(f"            <code>{esc(code)}</code>")
        lines.extend(
            [
                f"            <span>{description}</span>",
                "          </a>",
            ]
        )
        output.append("\n".join(lines))
    return "\n".join(output)


def story_status(items: list[dict[str, Any]]) -> str:
    output = []
    for item in items:
        output.append(
            f"""
          <div class="status-item"><strong>{esc(item.get("label"))}</strong><span>{esc(item.get("description"))}</span></div>"""
        )
    return "\n".join(output)


def commands(items: list[str]) -> str:
    return "\n".join(f"          <code>{esc(item)}</code>" for item in items)


def review_items(items: list[str]) -> str:
    return "\n".join(f"          <li>{esc(item)}</li>" for item in items)


